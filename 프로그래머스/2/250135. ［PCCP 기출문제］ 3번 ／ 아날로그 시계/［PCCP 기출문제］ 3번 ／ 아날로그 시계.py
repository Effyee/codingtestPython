def solution(h1, m1, s1, h2, m2, s2):
    
    answer = 0
    
    # 초침이 분침을 지나간 횟수 / 시침을 지나간 횟수
    mcount, hcount = 0, 0

    # 시작 시간과 끝 시간을 "초 단위"로 변환
    second1 = h1*60*60 + m1*60 + s1
    second2 = h2*60*60 + m2*60 + s2
    
    # 시작 시각이 정확히 12:00:00 또는 00:00:00이면
    # 초침, 분침, 시침이 모두 겹치므로 알람 1번
    if second1 == 0 or second1 == 60*60*12 :
        answer += 1
    
    # 매 초마다 시뮬레이션
    # t초 → t+1초 사이에서 겹침이 발생했는지 확인
    for i in range(second1, second2):

        # 현재 시각에서의 각도
        # 초침 : 1초에 6도
        s = (i*6) % 360
        
        # 분침 : 1초에 0.1도 (360도 / 3600초)
        m = (i/10) % 360
        
        # 시침 : 1초에 1/120도 (360도 / 43200초)
        h = (i/120) % 360

        # 다음 1초 뒤 각도
        # 0도는 비교가 불편하므로 360도로 변환
        ns = 360 if (i+1)*6 % 360 == 0 else (i+1)*6 % 360
        nm = 360 if (i+1)/10 % 360 == 0 else (i+1)/10 % 360
        nh = 360 if (i+1)/120 % 360 == 0 else (i+1)/120 % 360
        
        # 초침이 시침을 지나가는 순간
        # 이전에는 초침이 뒤에 있었고
        # 다음에는 초침이 시침 이상 위치에 있음
        if s < h and ns >= nh:
            hcount += 1

        # 초침이 분침을 지나가는 순간
        if s < m and ns >= nm:
            mcount += 1

        # 세 바늘이 동시에 겹치는 순간
        # (12:00:00 같은 경우)
        # 위에서 두 번 카운트됐을 수 있으므로 1번 빼줌
        if ns == nm == nh:
            answer -= 1

    # 초침-분침 만남 + 초침-시침 만남
    answer += mcount + hcount

    return answer